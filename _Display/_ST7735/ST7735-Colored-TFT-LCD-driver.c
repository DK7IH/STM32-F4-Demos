//Define window area for next graphic operation
void lcd_setwindow(int x0, int y0, int x1, int y1)
{
	lcd_write_command(ST7735_CASET);   //Coloumn address set
	lcd_write_data(0x00);
	lcd_write_data(x0);          
	lcd_write_data(0x00);
	lcd_write_data(x1);          

	lcd_write_command(ST7735_RASET);   //Row address set
	lcd_write_data(0x00);
	lcd_write_data(y0);        
	lcd_write_data(0x00);
	lcd_write_data(y1);        
}

